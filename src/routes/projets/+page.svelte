<h2>cvForms</h2>
<p>cvForms est un formulaire réalisé avec XForms et le client XSLTForms afin de rédiger et enregister son curriculum vitæ au format XML. Une feuille de style CSS permet également de l'imprimer ou de la sauvegarder en PDF.</p>
<p>cvForms is web form made with XForms and XSLTForms client to write and store curriculum vitæ in XML Format. A ready to print CSS allows you to save or print your curriculum vitæ as PDF.</p>
